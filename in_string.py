def check_vowels():
    txt = input("")

    print("Contiene a: " + str("a" in txt.lower()))

    print("Contiene e: " + str("e" in txt.lower()))

    print("Contiene i: " + str("i" in txt.lower()))

    print("Contiene o: " + str("o" in txt.lower()))

    print("Contiene u: " + str("u" in txt.lower()))
